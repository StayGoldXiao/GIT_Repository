import math

# 输入弦AB的长度与弓CD的高度，浮点数
# =====================================================
AB_len = float(input())
CD_len = float(input())
# =====================================================

# 计算半弦长
# =====================================================
AD_len = AB_len / 2
# =====================================================

# 计算半径
# =====================================================
OA_len = (AD_len ** 2 + CD_len ** 2) / (CD_len * 2)
# =====================================================

# 计算圆心角
# =====================================================
AOB = 2 * math.asin(AD_len / OA_len)
# =====================================================

# 计算弓形所在扇形的面积
# =====================================================
area_of_sector = AOB / (math.pi * 2) * math.pi * OA_len ** 2
# =====================================================

# 计算三角形面积
# =====================================================
area_of_triangle = 0.5 * OA_len ** 2 * math.sin(AOB)
# =====================================================

# 计算弓形面积
# =====================================================
area_of_arch = area_of_sector - area_of_triangle
# =====================================================

# 在两行中分别输出半径和弓形面积
# =====================================================
print(f"半径为: {OA_len:.2f}")
print(f"弓形面积为: {area_of_arch:.2f}")
# =====================================================
